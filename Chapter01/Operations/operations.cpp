#include <iostream>

using namespace std;

int main() {
	int a = 3, b = 5;
	double c = 3.14, d = 5.26;

	cout << "a = " << a << endl;
	cout << "b = " << b << endl;
	cout << "c = " << c << endl;
	cout << "d = " << d << endl << endl;

	cout << "a + b = " << a + b << endl;
	cout << "a - c = " << a - c << endl;
	cout << "a * d = " << a * d << endl;
	cout << "a / b = " << (double)a / b << endl;
	return 0;
}
